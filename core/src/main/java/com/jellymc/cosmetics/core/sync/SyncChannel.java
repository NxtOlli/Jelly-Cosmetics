package com.jellymc.cosmetics.core.sync;

/**
 * Enum representing the different synchronization channels used by the plugin
 */
public enum SyncChannel {
    /**
     * Channel for cosmetic-related messages
     */
    COSMETIC,

    /**
     * Channel for chat color-related messages
     */
    CHAT_COLOR,

    /**
     * Channel for name paint-related messages
     */
    NAME_PAINT,

    /**
     * Channel for auction house-related messages
     */
    AUCTION,

    /**
     * Channel for resource pack-related messages
     */
    RESOURCE_PACK
}
